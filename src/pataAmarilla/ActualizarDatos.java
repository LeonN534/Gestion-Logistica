    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nuevoTelefono = jTextField3.getText();
        String nuevoCorreo = jTextField4.getText();
        String nuevaDireccion = jTextField5.getText();

        try{
            Connection conexion = DriverManager.getConnection(URL, USERNAME, PASSWORD);
            String sqlUpdate = "UPDATE trabajador SET telefono = ?, correo = ?, direccion = ? WHERE dni = ?";
            try (PreparedStatement statement = conexion.prepareStatement(sqlUpdate)){
                statement.setString(1, jTextField3.getText());
                statement.setString(2, jTextField4.getText());
                statement.setString(3, jTextField5.getText());
                statement.setString(4, dni);
                int fileasAct = statement.executeUpdate();
                if(fileasAct > 0 ) {
                    JOptionPane.showMessageDialog(this, "Datos actualizada");
                    this.dispose();
                    MenuPrincipal ventanaMenu = new MenuPrincipal();
                    ventanaMenu.setLocationRelativeTo(null);
                    ventanaMenu.setVisible(true);
                }
            } 
        } catch (SQLException e){
            JOptionPane.showMessageDialog(this, "Error al modificar los datos. " + e.getMessage());
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    
